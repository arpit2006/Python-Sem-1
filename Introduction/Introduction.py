print(5 + 2)   # addition(+)
print(5 - 2)   # subtraction(-)
print(5 * 2)   # multiplication(*)
print(5 / 2)   # division(/)
print(5 ** 2)  # exponential(**)
print(5 % 2)   # modulus(%)
print(5 // 2)  # Floor division operator(//)

# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 4j))              # Complex
print(type('XYX'))          # String
print(type([1, 2, 3]))           # List
print(type({'name':'XYX'})) # Dictionary
print(type({9, 3.1, 2}))    # Set
print(type((9, 3, 2)))    # Tuple
print(type(3 == 3))              # Bool
print(type(3 >= 3))              # Bool